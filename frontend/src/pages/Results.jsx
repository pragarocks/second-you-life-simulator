import React, { useState } from 'react';
import { useLocation, Navigate } from 'react-router-dom';
import TimelineView from '../components/TimelineView';
import ComparisonView from '../components/ComparisonView';
import { Clock, GitCompare, Download, Share2 } from 'lucide-react';

const Results = () => {
  const location = useLocation();
  const [activeView, setActiveView] = useState('timeline');

  // Redirect if no simulation data
  if (!location.state?.alternatePath || !location.state?.currentPath) {
    return <Navigate to="/" replace />;
  }

  const { alternatePath, currentPath, originalData } = location.state;

  const handleShare = async () => {
    if (navigator.share) {
      try {
        await navigator.share({
          title: 'Second You - My Life Simulation',
          text: 'Check out my alternate life path simulation!',
          url: window.location.href,
        });
      } catch (error) {
        console.log('Error sharing:', error);
      }
    } else {
      // Fallback: copy to clipboard
      navigator.clipboard.writeText(window.location.href);
      alert('Link copied to clipboard!');
    }
  };

  const handleDownload = () => {
    const content = `
SECOND YOU - LIFE SIMULATION RESULTS
=====================================

Original Profile:
- Age: ${originalData.age}
- Location: ${originalData.location}
- Profession: ${originalData.profession}
- Traits: ${originalData.traits}

Alternate Path Explored:
${originalData.alternatePath}

ALTERNATE PATH SIMULATION:
=========================

1 YEAR LATER:
${alternatePath.year1}

3 YEARS LATER:
${alternatePath.year3}

10 YEARS LATER:
${alternatePath.year10}

Future Self's Message (Alternate Path):
${alternatePath.futureMessage}

CURRENT PATH SIMULATION:
=======================

1 YEAR LATER:
${currentPath.year1}

3 YEARS LATER:
${currentPath.year3}

10 YEARS LATER:
${currentPath.year10}

Future Self's Message (Current Path):
${currentPath.futureMessage}

Generated by Second You - Life Co-Simulator
    `;

    const blob = new Blob([content], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'my-life-simulation.txt';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };

  return (
    <div className="max-w-6xl mx-auto">
      {/* Header */}
      <div className="text-center mb-8">
        <h1 className="text-4xl font-bold bg-gradient-to-r from-primary-600 to-secondary-600 bg-clip-text text-transparent mb-4">
          Your Alternate Life Path
        </h1>
        <p className="text-xl text-slate-600 mb-6">
          Here's what your life might look like if you chose: <br />
          <span className="font-medium text-slate-800">"{originalData.alternatePath}"</span>
        </p>

        {/* Action Buttons */}
        <div className="flex flex-wrap justify-center gap-4 mb-8">
          <button
            onClick={handleDownload}
            className="flex items-center space-x-2 bg-slate-100 hover:bg-slate-200 text-slate-700 px-4 py-2 rounded-lg transition-colors duration-200"
          >
            <Download className="h-4 w-4" />
            <span>Download Results</span>
          </button>
          <button
            onClick={handleShare}
            className="flex items-center space-x-2 bg-slate-100 hover:bg-slate-200 text-slate-700 px-4 py-2 rounded-lg transition-colors duration-200"
          >
            <Share2 className="h-4 w-4" />
            <span>Share</span>
          </button>
        </div>

        {/* View Toggle */}
        <div className="flex justify-center mb-8">
          <div className="bg-white rounded-lg p-1 shadow-md border border-slate-200">
            <button
              onClick={() => setActiveView('timeline')}
              className={`flex items-center space-x-2 px-4 py-2 rounded-md transition-colors duration-200 ${
                activeView === 'timeline'
                  ? 'bg-primary-500 text-white'
                  : 'text-slate-600 hover:text-slate-800'
              }`}
            >
              <Clock className="h-4 w-4" />
              <span>Timeline View</span>
            </button>
            <button
              onClick={() => setActiveView('comparison')}
              className={`flex items-center space-x-2 px-4 py-2 rounded-md transition-colors duration-200 ${
                activeView === 'comparison'
                  ? 'bg-primary-500 text-white'
                  : 'text-slate-600 hover:text-slate-800'
              }`}
            >
              <GitCompare className="h-4 w-4" />
              <span>Comparison View</span>
            </button>
          </div>
        </div>
      </div>

      {/* Content */}
      <div className="animate-fade-in">
        {activeView === 'timeline' ? (
          <TimelineView simulation={alternatePath} />
        ) : (
          <ComparisonView 
            alternatePath={alternatePath} 
            currentPath={currentPath}
            originalData={originalData} 
          />
        )}
      </div>

      {/* Future Message - Only for Timeline View */}
      {activeView === 'timeline' && alternatePath.futureMessage && (
        <div className="mt-12 card max-w-3xl mx-auto text-center">
          <h3 className="text-xl font-semibold text-slate-800 mb-4">
            Message from Your Future Self
          </h3>
          <blockquote className="text-lg text-slate-700 italic leading-relaxed">
            "{alternatePath.futureMessage}"
          </blockquote>
        </div>
      )}
    </div>
  );
};

export default Results; 